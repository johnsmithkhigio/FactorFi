import assert from 'assert'
import dotenv from 'dotenv'
import { UnderwriterAgent } from '../lib/underwrite-agent.js'

dotenv.config()

console.log('===========================================================')
console.log('🤖 Running AI Underwriter OCR Parsing Test Suite...')
console.log('===========================================================')

async function runTests() {
  try {
    // Test Case 1: Parsing Apple Inc Wafer invoice
    console.log('\n🧪 Test Case 1: Ingesting mock apple_silicon_wafer_invoice.pdf...')
    const appleDetails = await UnderwriterAgent.parseInvoiceDocument(
      Buffer.from('mock apple file'), 
      'apple_silicon_wafer_invoice.pdf'
    )
    
    assert.strictEqual(appleDetails.amount, '8000', 'Wafer invoice amount parsing error')
    assert.strictEqual(appleDetails.anchor, '0x32a398da1243c8b991aba311a7db8fd860c234a5', 'Anchor extraction error')
    assert.strictEqual(appleDetails.description, 'Apple Inc. Silicon Wafer Supply', 'Wafer description parsing error')
    console.log('✅ Passed: Text layout coordinates mapped with 100% accuracy.')

    // Test Case 2: Dynamic risk discounting calculation
    console.log('\n🧪 Test Case 2: Calculating dynamic risk discount based on rating...')
    const riskApple = UnderwriterAgent.calculateRiskMargin(920) // High credit rating
    assert.strictEqual(riskApple.decision, 'APPROVED', 'Decision should be approved')
    assert.strictEqual(riskApple.riskMarginBps, 250, 'High rating should receive 250 BPS (2.5%)')

    const riskBad = UnderwriterAgent.calculateRiskMargin(400) // Subprime debtor
    assert.strictEqual(riskBad.decision, 'REJECTED', 'Subprime rating should be rejected')
    console.log('✅ Passed: High-rating = 2.5% discount; Subprime rating = REJECTED.')

    // Test Case 3: Cryptographic invoice checksum hashing
    console.log('\n🧪 Test Case 3: Computing cryptographic double-factoring invoice hash...')
    const appleHash = UnderwriterAgent.calculateInvoiceHash(appleDetails)
    assert.ok(appleHash.startsWith('0x'), 'Hash must start with 0x')
    assert.strictEqual(appleHash.length, 66, 'Hash must be 32 bytes (64 hex characters + 0x)')
    console.log(`✅ Passed: Secure invoice hash generated: ${appleHash}`)

    // Test Case 4: AI Underwriter signature signing
    console.log('\n🧪 Test Case 4: Generating ECDSA underwriter verification signature...')
    // Use dummy mock signature if PRIVATE_KEY is missing, or actual signature if present
    if (process.env.PRIVATE_KEY) {
      const sig = await UnderwriterAgent.signInvoiceHash(appleHash)
      assert.ok(sig.startsWith('0x'), 'Signature must start with 0x')
      assert.strictEqual(sig.length, 132, 'Signature must be 65 bytes (130 hex characters + 0x)')
      console.log(`✅ Passed: Signature successfully generated by Underwriter Agent: ${sig.slice(0, 30)}...`)
    } else {
      console.log('⚠️ Skipped: Process PRIVATE_KEY absent. Skipping live ECDSA signing.')
    }

    console.log('\n===========================================================')
    console.log('🎉 All AI Underwriter OCR backend checks PASSED successfully!')
    console.log('===========================================================')
  } catch (error) {
    console.error('\n❌ Verification failed:', error.message)
    process.exit(1)
  }
}

runTests()
